# string slicing

"""CRUD : Retrieval
state : string
Behaviour : if,elif, ==,for"""

statement = 'hi, good morning. how are you'
print('printing hi---', statement[0:2])
print('printing morning---', statement[9:16])
print('printing how are you---', statement[18:])
print('printing letters at even index position --- ', statement[0:: 2])

statement = "hello satish"
print("print the hello : ",statement[0:])
